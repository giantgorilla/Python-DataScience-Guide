#3.1.4.3-D Diziler

#Elemanları olarak 2 boyutlu dizilere (matrisler) sahip olan bir dizi, 3 boyutlu dizi olarak adlandırılır. Bunlar genellikle 3. dereceden bir tensörü temsil etmek için kullanılır.

