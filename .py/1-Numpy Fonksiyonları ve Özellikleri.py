#3.1Dizilerde Boyutlar

#Dizilerdeki bir boyut, bir dizi derinliği düzeyidir(iç içe diziler).

#3.1.1.0-D Diziler
#0-D dizileri veya skalarlar, bir dizideki öğelerdir. Bir dizideki her değer bir 0-D dizisidir.

