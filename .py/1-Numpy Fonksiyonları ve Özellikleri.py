#15.Arange Fonksiyonu:
#Arange fonksiyonu Python'daki range() fonksiyonuyla neredeyse aynıdır. Parantezlerin içerisinde yer alan üç parametreden birincisi başlangıç sayısını, ikincisi durulacak sayıyı, üçüncüsü ise iki sayı arasındaki sayıların kaçar kaçar geçileceğidir.

